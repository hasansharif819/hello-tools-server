import { Schema, model } from 'mongoose';
import { TMessages } from './messages.interface';

const messagesSchema = new Schema<TMessages>(
  {
    name: {
      type: String,
      trim: true,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    contact: {
      type: String,
      required: true,
    },
    message: {
      type: String,
      required: true,
    },
    isDeleted: {
      type: Boolean,
      default: false,
    },
  },
  {
    timestamps: true,
  },
);

export const Messages = model<TMessages>('Messages', messagesSchema);
